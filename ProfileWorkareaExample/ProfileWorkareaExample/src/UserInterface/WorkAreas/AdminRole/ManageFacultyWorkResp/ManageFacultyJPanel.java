    private void btnAddFacultyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddFacultyActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String title = txtFacultyTitle.getText();
        String username = txtUserName.getText();
        String password = txtPassword.getText();

        if (name.isEmpty() || username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name, user name and password are required.",
                    "Missing Information", JOptionPane.WARNING_MESSAGE);
            return;
        }

        UserAccountDirectory uad = business.getUserAccountDirectory();
        if (uad.isUsernameTaken(username)) {
            JOptionPane.showMessageDialog(this, "That user name is already taken.",
                    "User Name Unavailable", JOptionPane.WARNING_MESSAGE);
            return;
        }

        PersonDirectory personDir = business.getPersonDirectory();
        Person person = personDir.newPerson(name);
        person.setName(name);

        FacultyDirectory fd = business.getFacultyDirectory();
        FacultyProfile fp = fd.newFacultyProfile(person);
        fp.setTitle(title);

        uad.newUserAccount(fp, username, password);

        JOptionPane.showMessageDialog(this, "Faculty member added with login " + username + ".",
                "Saved", JOptionPane.INFORMATION_MESSAGE);

        txtName.setText("");
        txtFacultyTitle.setText("");
        txtUserName.setText("");
        txtPassword.setText("");
        refreshTable();
        
        
    }//GEN-LAST:event_btnAddFacultyActionPerformed

    private void tblFacultyMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblFacultyMousePressed
        // Extracts the row (uaser account) in the table that is selected by the user
        int size = tblFaculty.getRowCount();
        int selectedrow = tblFaculty.getSelectionModel().getLeadSelectionIndex();

        if (selectedrow < 0 || selectedrow > size - 1) {
            return;
        }
        selectedFaculty = (FacultyProfile) tblFaculty.getValueAt(selectedrow, 0);
        if (selectedFaculty == null) {
            return;
        }

    }//GEN-LAST:event_tblFacultyMousePressed

    private void btnDeleteSelectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteSelectedActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblFaculty.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a faculty member first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        FacultyProfile fp = (FacultyProfile) tblFaculty.getValueAt(selectedrow, 0);
        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete faculty " + fp.getPerson().getName() + " (and their login)?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            UserAccount ua = business.getUserAccountDirectory().findUserAccount(fp.getPerson().getPersonId());
            if (ua != null) {
                business.getUserAccountDirectory().removeUserAccount(ua);
            }
            business.getFacultyDirectory().removeFaculty(fp);
            refreshTable();
            JOptionPane.showMessageDialog(this, "Faculty member deleted.",
                    "Deleted", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteSelectedActionPerformed

