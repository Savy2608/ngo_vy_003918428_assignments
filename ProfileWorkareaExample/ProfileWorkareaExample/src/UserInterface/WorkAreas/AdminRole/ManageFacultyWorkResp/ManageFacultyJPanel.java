    private void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblFaculty.getModel();
        model.setRowCount(0);

        FacultyDirectory fd = business.getFacultyDirectory();
        UserAccountDirectory uad = business.getUserAccountDirectory();

        for (FacultyProfile fp : fd.getFacultyList()) {
            UserAccount ua = uad.findUserAccount(fp.getPerson().getPersonId());

            String login = "(no login)";
            if (ua != null) {
                login = ua.getUserLoginName();
            }

            Object[] row = new Object[3];
            row[0] = fp;
            row[1] = fp.getTitle();
            row[2] = login;
            model.addRow(row);
        }
    
    }
