    public void refreshTable() {

//clear supplier table
        int rc = tblUserAccounts.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) tblUserAccounts.getModel()).removeRow(i);
        }

        UserAccountDirectory uad = business.getUserAccountDirectory();

        for (UserAccount ua : uad.getUserAccountList()) {

            Object[] row = new Object[5];
            row[0] = ua;
            row[1] = ua.getRole();
            row[2] = ua.getAssociatedPersonProfile().getPerson().getName();
            row[3] = ua.getLastAccessText();
            row[4] = ua.getLastUpdatedText();
            ((DefaultTableModel) tblUserAccounts.getModel()).addRow(row);
        }

    }

