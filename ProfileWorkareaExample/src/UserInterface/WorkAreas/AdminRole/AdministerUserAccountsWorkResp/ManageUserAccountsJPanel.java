    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblUserAccounts.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(this, "Please select an account first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        selecteduseraccount = (UserAccount) tblUserAccounts.getValueAt(selectedrow, 0);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete account " + selecteduseraccount.getUserLoginName() + "?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            business.getUserAccountDirectory().removeUserAccount(selecteduseraccount);
            refreshTable();
            JOptionPane.showMessageDialog(this, "Account deleted.",
                    "Deleted", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

