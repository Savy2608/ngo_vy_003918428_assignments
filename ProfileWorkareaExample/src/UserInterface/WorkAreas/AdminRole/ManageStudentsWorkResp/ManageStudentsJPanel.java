    private void tblStudentMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMousePressed
        // TODO add your handling code here:
        int size = tblStudent.getRowCount();
        int selectedrow = tblStudent.getSelectionModel().getLeadSelectionIndex();

        if (selectedrow < 0 || selectedrow > size - 1) {
            return;
        }
        selectedStudent = (StudentProfile) tblStudent.getValueAt(selectedrow, 0);
        if (selectedStudent == null) {
            return;
        }
    }//GEN-LAST:event_tblStudentMousePressed

    private void btnAddNUIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNUIDActionPerformed
        // TODO add your handling code here:
        String nuid = txtNUID.getText().trim();

        if (nuid.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter an NUID.",
                    "Missing Information", JOptionPane.WARNING_MESSAGE);
            return;
        }

        StudentDirectory sd = business.getStudentDirectory();
        if (sd.findStudentByNuid(nuid) != null) {
            JOptionPane.showMessageDialog(this, "A student with that NUID already exists.",
                    "Duplicate NUID", JOptionPane.WARNING_MESSAGE);
            return;
        }


        PersonDirectory personDir = business.getPersonDirectory();
        Person person = personDir.newPerson(nuid); // NUID is the unique person id
        person.setNuid(nuid);
        person.setName("");
        sd.newStudentProfile(person);

        JOptionPane.showMessageDialog(this,
                "NUID " + nuid + " added. The student can now sign up with this NUID.",
                "Saved", JOptionPane.INFORMATION_MESSAGE);

        txtNUID.setText("");
        refreshTable();
    
    }//GEN-LAST:event_btnAddNUIDActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         int selectedrow = tblStudent.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a student first.",
                    "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }
        StudentProfile sp = (StudentProfile) tblStudent.getValueAt(selectedrow, 0);
        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete student " + sp.getPerson().getName() + " (and any login)?",
                "Confirm Delete", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            UserAccount ua = business.getUserAccountDirectory().findUserAccount(sp.getPerson().getPersonId());
            if (ua != null) {
                business.getUserAccountDirectory().removeUserAccount(ua);
            }
            business.getStudentDirectory().removeStudent(sp);
            refreshTable();
            JOptionPane.showMessageDialog(this, "Student deleted.",
                    "Deleted", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

