    private void refreshTable() {
       DefaultTableModel model = (DefaultTableModel) tblStudent.getModel();
       model.setRowCount(0);

       StudentDirectory sd = business.getStudentDirectory();
       UserAccountDirectory uad = business.getUserAccountDirectory();

       for (StudentProfile sp : sd.getStudentList()) {
            UserAccount ua = uad.findUserAccount(sp.getPerson().getPersonId());

            String status = "Pending sign-up";
            if (ua != null) {
                status = "Registered";
            }

            Object[] row = new Object[3];
            row[0] = sp;
            row[1] = sp.getPerson().getNuid();
            row[2] = status;
            model.addRow(row);
        }
    }
